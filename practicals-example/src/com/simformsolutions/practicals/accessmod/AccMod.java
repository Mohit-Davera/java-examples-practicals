package com.simformsolutions.practicals.accessmod;

public class AccMod {
	public String pubVar;
	private String priVar;
	protected String proVar;
	public String getPubVar() {
		return pubVar;
	}
	public void setPubVar(String pubVar) {
		this.pubVar = pubVar;
	}
	public String getPriVar() {
		return priVar;
	}
	public void setPriVar(String priVar) {
		this.priVar = priVar;
	}
	public String getProVar() {
		return proVar;
	}
	public void setProVar(String proVar) {
		this.proVar = proVar;
	}
	
}
